# -*- encoding : utf-8 -*-
class Video < Archivo

  ##############################################################################
  #### CONFIGURACIONES Y RELACIONES
  ##############################################################################

  attr_accessible :codigo

  ##############################################################################
  #### SCOPES Y VALIDACIONES
  ##############################################################################

  validates :codigo, :presence => true

  ##############################################################################
  #### MÉTODOS PÚBLICOS
  ##############################################################################

  def thumbnail_url
    File.join 'http://img.youtube.com/vi', codigo.to_s, 'default.jpg'
  end

  ##############################################################################
  #### ALIAS E IMPRESIONES
  ##############################################################################

  ##############################################################################
  #### MÉTODOS PRIVADOS
  ##############################################################################

  private

end
